'use strict';

(function($){
	/*****************************************************************************************************/
	/* One Page Nav */
	/*****************************************************************************************************/
	jQuery('#navigation a[href^="#"]').on('click',function (e) {
		e.preventDefault();
		jQuery('html, body').stop().animate({
			'scrollTop': jQuery(jQuery(this).attr('href')).offset().top - 160
		}, 500, 'swing', function () {});
	});

	/*****************************************************************************************************/
	/* Slider */
	/*****************************************************************************************************/
	$('.slides').superslides({
		play: 4000 
	});

	/*****************************************************************************************************/
	/* Wow Js Animation */
	/*****************************************************************************************************/

	new WOW({
		callback: function(box) {
			// $(box).css('position', 'absolute');
			//console.log($(box).css('position', 'absolute'));
		}
	}).init();

	/*****************************************************************************************************/
	/* Parallax Scrolling */
	/*****************************************************************************************************/
	$(window).stellar();


	/*****************************************************************************************************/
	/* Sticky Header */
	/*****************************************************************************************************/
	$(window).scroll(function() {

		if ($(this).scrollTop() > 1){  
		    $('.main-nav-container ').addClass("sticky");
		  }
		else{
		    $('.main-nav-container').removeClass("sticky");
		  }

	});

	/*****************************************************************************************************/
	/* Envelop Animation */
	/*****************************************************************************************************/
	var eWrapper = $('.envelop-wrapper');
	var letter = $('.letter');
	var offset = 200;
	$(window).scroll(function() {
		if ((eWrapper.offset().top - $(window).height() + offset) < $(this).scrollTop()) {
			letter.addClass('letter-up');
		} else {
			letter.removeClass('letter-up');
		}
	});
})(jQuery);